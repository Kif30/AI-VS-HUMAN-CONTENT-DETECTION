"use client"

import type React from "react"

import { useEffect, useState, useCallback } from "react"
import { useRouter } from "next/navigation"
import { Button } from "@/components/ui/button"
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { ArrowLeft, Music, Upload, Loader2, X } from "lucide-react"

export default function AudioUploadPage() {
  const [selectedFile, setSelectedFile] = useState<File | null>(null)
  const [preview, setPreview] = useState<string | null>(null)
  const [isDragging, setIsDragging] = useState(false)
  const [isAnalyzing, setIsAnalyzing] = useState(false)
  const router = useRouter()

  useEffect(() => {
    const isAuthenticated = localStorage.getItem("isAuthenticated")
    if (!isAuthenticated) {
      router.push("/")
    }
  }, [router])

  const handleFileSelect = useCallback((file: File) => {
    if (file && file.type.startsWith("audio/")) {
      setSelectedFile(file)
      const reader = new FileReader()
      reader.onload = (e) => {
        setPreview(e.target?.result as string)
      }
      reader.readAsDataURL(file)
    }
  }, [])

  const handleDrop = useCallback(
    (e: React.DragEvent) => {
      e.preventDefault()
      setIsDragging(false)
      const files = Array.from(e.dataTransfer.files)
      if (files.length > 0) {
        handleFileSelect(files[0])
      }
    },
    [handleFileSelect],
  )

  const handleDragOver = useCallback((e: React.DragEvent) => {
    e.preventDefault()
    setIsDragging(true)
  }, [])

  const handleDragLeave = useCallback((e: React.DragEvent) => {
    e.preventDefault()
    setIsDragging(false)
  }, [])

  const handleFileInput = (e: React.ChangeEvent<HTMLInputElement>) => {
    const files = e.target.files
    if (files && files.length > 0) {
      handleFileSelect(files[0])
    }
  }

  const handleAnalyze = async () => {
    if (!selectedFile) return

    setIsAnalyzing(true)

    // Simulate analysis
    setTimeout(() => {
      localStorage.setItem(
        "analysisData",
        JSON.stringify({
          type: "audio",
          fileName: selectedFile.name,
          fileSize: selectedFile.size,
          timestamp: new Date().toISOString(),
        }),
      )
      router.push("/result")
    }, 3500)
  }

  const clearFile = () => {
    setSelectedFile(null)
    setPreview(null)
  }

  return (
    <div className="min-h-screen bg-background">
      <div className="p-6 lg:p-8">
        <div className="max-w-4xl mx-auto">
          {/* Header */}
          <div className="flex items-center space-x-4 mb-8">
            <Button variant="ghost" size="sm" onClick={() => router.push("/dashboard")} className="hover:bg-accent/10">
              <ArrowLeft className="h-4 w-4 mr-2" />
              Back to Dashboard
            </Button>
            <div className="flex items-center space-x-2">
              <div className="p-2 bg-orange-50 rounded-lg">
                <Music className="h-6 w-6 text-orange-600" />
              </div>
              <div>
                <h1 className="text-2xl font-bold text-foreground">Audio Analysis</h1>
                <p className="text-muted-foreground">Detect AI-generated audio content</p>
              </div>
            </div>
          </div>

          {/* Upload Card */}
          <Card className="border-border">
            <CardHeader>
              <CardTitle className="flex items-center space-x-2">
                <Music className="h-5 w-5" />
                <span>Audio Content Analysis</span>
              </CardTitle>
              <CardDescription>Upload an audio file to analyze if it was generated by AI</CardDescription>
            </CardHeader>
            <CardContent className="space-y-6">
              {!selectedFile ? (
                <div
                  className={`border-2 border-dashed rounded-lg p-8 text-center transition-colors ${
                    isDragging ? "border-accent bg-accent/5" : "border-border hover:border-accent/50"
                  }`}
                  onDrop={handleDrop}
                  onDragOver={handleDragOver}
                  onDragLeave={handleDragLeave}
                >
                  <Music className="h-12 w-12 text-muted-foreground mx-auto mb-4" />
                  <h3 className="text-lg font-medium text-foreground mb-2">Drop your audio file here</h3>
                  <p className="text-muted-foreground mb-4">or click to browse files</p>
                  <input type="file" accept="audio/*" onChange={handleFileInput} className="hidden" id="file-input" />
                  <Button asChild variant="outline">
                    <label htmlFor="file-input" className="cursor-pointer">
                      <Upload className="mr-2 h-4 w-4" />
                      Choose Audio
                    </label>
                  </Button>
                  <p className="text-xs text-muted-foreground mt-2">Supports MP3, WAV, M4A up to 50MB</p>
                </div>
              ) : (
                <div className="space-y-4">
                  <div className="relative bg-muted rounded-lg p-6">
                    <audio src={preview!} controls className="w-full">
                      Your browser does not support the audio tag.
                    </audio>
                    <Button variant="destructive" size="sm" className="absolute top-2 right-2" onClick={clearFile}>
                      <X className="h-4 w-4" />
                    </Button>
                  </div>
                  <div className="text-center">
                    <p className="text-sm text-muted-foreground">
                      {selectedFile.name} ({(selectedFile.size / 1024 / 1024).toFixed(2)} MB)
                    </p>
                  </div>
                </div>
              )}

              <div className="flex justify-end space-x-4">
                <Button variant="outline" onClick={clearFile} disabled={!selectedFile || isAnalyzing}>
                  Clear
                </Button>
                <Button
                  onClick={handleAnalyze}
                  disabled={!selectedFile || isAnalyzing}
                  className="bg-primary hover:bg-primary/90"
                >
                  {isAnalyzing ? (
                    <>
                      <Loader2 className="mr-2 h-4 w-4 animate-spin" />
                      Analyzing...
                    </>
                  ) : (
                    <>
                      <Upload className="mr-2 h-4 w-4" />
                      Analyze Audio
                    </>
                  )}
                </Button>
              </div>
            </CardContent>
          </Card>
        </div>
      </div>
    </div>
  )
}
